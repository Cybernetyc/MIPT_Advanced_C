/**
 * @file main.c
 * @brief Вычисляет количество различных трехзначных чисел, которые можно получить,
 *        вычеркивая цифры из десятичной записи заданного натурального N.
 *
 * @author Черкашин Дмитрий
 */

#include <stdio.h>
#include <string.h>

/**
 * @brief Основная функция программы.
 *
 * @return 0, если программа завершена успешно.
 */
int main() {
  /**
   * @brief Хранилище десятичной записи заданного натурального N.
   */
  char N[1000];
  scanf("%s", N);
  int len = strlen(N);

  /**
   * @brief Если длина десятичной записи меньше 3, выводим 0 и завершаем программу.
   */
  if (len < 3) {
    printf("0\n");
    return 0;
  }

  /**
   * @brief Массив, в котором фиксируются использованные числа.
   */
  int used[1000] = {0};
  int count = 0;

  /**
   * @brief Цикл по всем цифрам десятичной записи N.
   */
  for (int i = 0; i < len; i++) {
    /**
     * @brief Пропускаем тройки, где первая цифра — ноль.
     */
    if (N[i] == '0') continue;

    /**
     * @brief Цикл по следующим двум цифрам десятичной записи N.
     */
    for (int j = i + 1; j < len; j++) {
      /**
       * @brief Цикл по последней цифре десятичной записи N.
       */
      for (int k = j + 1; k < len; k++) {
        /**
         * @brief Составляем трехзначное число из выбранных цифр.
         */
        int num = (N[i] - '0') * 100 + (N[j] - '0') * 10 + (N[k] - '0');

        /**
         * @brief Если число еще не использовалось, фиксируем его и увеличиваем счетчик.
         */
        if (!used[num]) {
          used[num] = 1;
          count++;
        }
      }
    }
  }

  /**
   * @brief Выводим результат и завершаем программу.
   */
  printf("%d\n", count);
  return 0;
}
